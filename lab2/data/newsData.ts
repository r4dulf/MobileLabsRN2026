export const generateNewsItems = (count: number, offset: number = 0): NewsItem[] =>
  Array.from({ length: count }, (_, i) => {
    const idx = offset + i;
    return {
      id: String(idx + 1),
      title: `${TITLES[idx % TITLES.length]} #${idx + 1}`,
      description: DESCRIPTIONS[idx % DESCRIPTIONS.length],
      image: `https://picsum.photos/seed/${idx + 1}/600/300`,
    };
  });
